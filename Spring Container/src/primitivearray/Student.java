package primitivearray;

public class Student {
	
	private int sid;
	
	private String sName;
	
	private String mono[];

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getsName() {
		return sName;
	}

	public void setsName(String sName) {
		this.sName = sName;
	}

	public String[] getMono() {
		return mono;
	}

	public void setMono(String[] mono) {
		this.mono = mono;
	}
	
	

}
